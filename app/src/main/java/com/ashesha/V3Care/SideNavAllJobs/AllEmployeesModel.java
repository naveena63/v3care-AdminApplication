package com.ashesha.V3Care.SideNavAllJobs;

public class AllEmployeesModel
{
    public String empId;
    public String empName;
    public String empEmail;
    public String empAddress;
    public String empMobileNum;

    public String getEmpMobileNum() {
        return empMobileNum;
    }

    public void setEmpMobileNum(String empMobileNum) {
        this.empMobileNum = empMobileNum;
    }

    public String getEmpId() {
        return empId;
    }

    public void setEmpId(String empId) {
        this.empId = empId;
    }

    public String getEmpName() {
        return empName;
    }

    public void setEmpName(String empName) {
        this.empName = empName;
    }

    public String getEmpEmail() {
        return empEmail;
    }

    public void setEmpEmail(String empEmail) {
        this.empEmail = empEmail;
    }

    public String getEmpAddress() {
        return empAddress;
    }

    public void setEmpAddress(String empAddress) {
        this.empAddress = empAddress;
    }
}
